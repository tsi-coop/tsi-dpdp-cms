    /**
     * @return an unmodifiable map of notification_type -> {language_code: message}, ready to
     * be upserted one row per type into notification_message_templates.
     */
    public static Map<String, Map<String, String>> getAll() {
        Map<String, Map<String, String>> out = new LinkedHashMap<>();
        for (Map.Entry<String, String[]> entry : RAW.entrySet()) {
            Map<String, String> byLang = new LinkedHashMap<>();
            String[] texts = entry.getValue();
            for (int i = 0; i < LANGS.length && i < texts.length; i++) {
                byLang.put(LANGS[i], texts[i]);
            }
            out.put(entry.getKey(), Collections.unmodifiableMap(byLang));
        }
        return Collections.unmodifiableMap(out);
    }

